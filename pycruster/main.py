import pycruster as pc
import numpy as np

from sklearn.cluster import KMeans

fake_data = np.array([
    [1, 2, 2, 2, 1, 2, 4, 2],
    [2, 2, 2, 1, 1, 2, 1, 1],
    [1, 2, 2, 2, 1, 2, 4, 2],
    [2, 2, 2, 1, 1, 2, 1, 1],
    [1, 2, 2, 2, 1, 2, 4, 2],
    [1, 2, 2, 2, 1, 2, 4, 2],
    [2, 2, 2, 1, 1, 2, 1, 1],
    [1, 2, 2, 2, 1, 2, 4, 2],
    [2, 2, 2, 1, 1, 2, 1, 1],
    [1, 2, 2, 2, 1, 2, 4, 2],
    [2, 2, 2, 1, 1, 2, 1, 1],
    [5, 4, 5, 4, 5, 2, 2, 2],
    [4, 5, 4, 5, 4, 2, 2, 2],
    [5, 4, 5, 4, 5, 2, 2, 2],
    [4, 5, 4, 5, 4, 2, 2, 2],
    [5, 4, 5, 4, 5, 2, 2, 2],
    [4, 5, 4, 5, 4, 6, 6, 5],
    [5, 4, 5, 4, 5, 6, 6, 6],
    [4, 5, 4, 5, 4, 6, 6, 5],
    [5, 4, 5, 4, 5, 6, 6, 4],
    ])

kmeans = KMeans(random_state=0)

elbow = pc.Elbow(estimator=kmeans, X=fake_data)

elbow.fit(min_k=2, max_k=10)

print(elbow.elbow)

